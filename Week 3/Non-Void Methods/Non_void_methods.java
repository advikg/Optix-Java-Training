public class Non_void_methods {
    public static String albertian_economics (String albert) {
        albert = albert + " is friends with Albert";
        return albert;
    }
    public static int doubling (int number) {
        number = number * 2;
        return number;
    }
    public static boolean is_optix_enjoyer (boolean common_optix_L) {
        return !common_optix_L;
    }
    public static void main (String[] args) {
        System.out.println(albertian_economics("Oddvik"));
        System.out.println(doubling(3749));
        System.out.println(is_optix_enjoyer(false));
    }
}
